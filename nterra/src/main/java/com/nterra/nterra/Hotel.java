package com.nterra.nterra;

public class Hotel {
	private int id;
	private String source;
	
	
	
	public Hotel(int id, String source) {
		super();
		this.id = id;
		this.source = source;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	
	
}
